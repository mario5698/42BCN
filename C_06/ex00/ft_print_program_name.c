#include<unistd.h>

int	main(int arg, char **argv)
{
	int	i;

	i = 0;
	if (arg == 1)
	{
		while (argv[0][i] != '\0')
		{
			write(1, &argv[0][i], 1);
			i++;
		}	
	}
	return (0);
}
